    private void simpan_alfianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan_alfianActionPerformed
        // TODO add your handling code here:
        if(nis_alfian.getText().equals("") || nama_alfian.getText().equals("") || bgk.getSelection().equals("")){
            JOptionPane.showMessageDialog(null, "Data Belum Lengkap!");
        }else{
        try{
            String jk;
            if(P.isSelected()){
                jk = "Pria";
            }else{
                jk = "Wanita";
            }
            st = cn.createStatement();
            st.executeUpdate("insert into tb_siswa set nis='" + nis_alfian.getText() +"', nama='" + nama_alfian.getText() +"', jkelamin='" + jk+ "', ket='belum'");
            tampilData();
            reset();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
        }catch(Exception e){
            e.printStackTrace();
            }
        }
    }//GEN-LAST:event_simpan_alfianActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        simpan_alfian.setEnabled(false);
        edit_alfian.setEnabled(true);
        hapus_alfian.setEnabled(true);
        nis_alfian.setText(tabModel.getValueAt(tabel.getSelectedRow(), 0) + "");
        nama_alfian.setText(tabModel.getValueAt(tabel.getSelectedRow(), 1) + "");
        String a = (tabModel.getValueAt(tabel.getSelectedRow(), 2) + "");
        if(a.equals("P")){
            P.setSelected(true);
        }else{
            W.setSelected(true);
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void edit_alfianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_alfianActionPerformed
        // TODO add your handling code here:
        try{
            simpan_alfian.setEnabled(true);
            String jk;
            if(P.isSelected()){
                jk = "Pria";
            }else{
                jk = "Wanita";
            }
            st = cn.createStatement();
            st.executeUpdate("update tb_siswa set nama='" + nama_alfian.getText() +"', jkelamin='" + jk+ "' where nis='"+ nis_alfian.getText() +"'");
            tampilData();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
            reset();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_edit_alfianActionPerformed

    private void hapus_alfianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapus_alfianActionPerformed
        // TODO add your handling code here:
        try{
            int jawab;
            if((jawab = JOptionPane.showConfirmDialog(null, "Ingin menghapus?", "konfirmasi", JOptionPane.YES_NO_OPTION)) == 0 ){
                st = cn.createStatement();
                st.executeUpdate("delete from tb_siswa where nis='" + nis_alfian.getText() + "'");
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
                tampilData();
                reset();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_hapus_alfianActionPerformed

